const AutoGeneratorService = require('../services/AutoGeneratorService');
const Order = require('../models/Order');
const Widget = require('../models/Widget');
const mongoose = require('mongoose');
const AIDashboardService = require('../services/AIDashboardService');

// @desc    Auto Generate Dashboard
// @route   POST /api/analytics/auto-generate
// @access  Public
exports.autoGenerateDashboard = async (req, res) => {
  try {
    const generatedDashboard = await AutoGeneratorService.generateDashboard();
    res.status(201).json(generatedDashboard);
  } catch (error) {
    console.error(error);
    res.status(500).json({ message: 'Error generating dashboard', error: error.message });
  }
};

// @desc    AI Generate Dashboard based on Prompt
// @route   POST /api/analytics/ai-generate
// @access  Private
exports.aiGenerateDashboard = async (req, res) => {
  try {
    const { prompt } = req.body;
    if (!prompt) return res.status(400).json({ message: 'Prompt is required' });
    
    const dashboard = await AIDashboardService.generateDashboardFromPrompt(prompt);
    res.status(201).json(dashboard);
  } catch (error) {
    const status = error.statusCode || 500;
    res.status(status).json({ message: error.message });
  }
};

