const express = require('express');
const router = express.Router();
const {
  autoGenerateDashboard,
  aiGenerateDashboard,
  aiAnalyzeRequirement,
  getWidgetData
} = require('../controllers/analyticsController');

const { protect } = require('../middlewares/authMiddleware');

router.get('/diag', (req, res) => {
  res.json({
    status: 'ok',
    hasKey: !!process.env.GEMINI_API_KEY,
    keyPrefix: process.env.GEMINI_API_KEY ? process.env.GEMINI_API_KEY.substring(0, 8) + '...' : 'none',
    timestamp: new Date().toISOString()
  });
});

router.use(protect);

router.post('/auto-generate', autoGenerateDashboard);
router.post('/ai-generate', aiGenerateDashboard);
router.post('/ai-analyze', aiAnalyzeRequirement);
router.post('/data', getWidgetData);

module.exports = router;
